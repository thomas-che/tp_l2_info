def ACM_Kruskal(G):
    """ calcule un ACM du graphe G en utilisant l'algorithme de Kruskal"""
    r = getAVertex()
    traites = set()
    arretes = []
    F = [r]
    while F:
        u = F.pop()
        for v in getNeighbors(u):
            if v not in traites:
                F.append(v)
                arretes.append(((u, v), getWeight(u, v)))
        traites.add(u)
    arretes = trier(arretes)
    S = UnionFind()
    w = 0
    for a in arretes:
        u = a[0][0]
        v = a[0][1]
        if S[u] != S[v]:
            w += a[1]
            S.union(u, v)
    SpanningTreeWeight(w)


def trier(t):
    """ trie une liste composée de couples de la forme ((u,v),poids de (u,v))"""
    n = len(t)
    if n <= 1:
        return t
    p = n // 2
    t1 = trier(t[:p])
    t2 = trier(t[p:])
    res = []
    for i in range(n):
        if t1 == []:
            res = res + t2
            break
        elif t2 == []:
            res = res + t1
            break
        elif t1[0][1] < t2[0][1]:
            res.append(t1[0])
            t1.pop(0)
        else:
            res.append(t2[0])
            t2.pop(0)
    return res


